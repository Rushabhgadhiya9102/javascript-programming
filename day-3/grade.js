// ---------- PRINTING THE GRADE USING AVERAGE ------------

let grade;
let avg = parseInt(prompt('Enter the Average'));

grade = avg >= 80 ? "A" : avg >= 60 ? "B" : avg >= 50 ? "C" : avg >= 40 ? "Pass" :"fail";

console.log("Grade is:",grade);

